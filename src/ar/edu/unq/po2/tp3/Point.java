package ar.edu.unq.po2.tp3;

public class Point {

	int x;
	int y;
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	public void puntoInicial() {
		this.setX(x);
		this.setY(y);
	}
	
	
	
}
